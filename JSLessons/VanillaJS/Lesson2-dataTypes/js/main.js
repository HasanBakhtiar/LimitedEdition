// Data Types (Data tipləri)

// Primitive DT -> Bəsit DT
// String
var a = "Hello";
var b = " JavaScript";
console.log(a + b);

// Number
console.log("510" + 5);

// Boolean
var e = true;
var x = false;
console.log(x);
// =========================================================

// Composite DT -> Birləşik DT
// Array
var myData = ["table", 4, true];
console.log(myData);
// Object
var myObj = { name: "Kamran", age: 20 };
console.log(myObj);
// =========================================================

// Special   DT -> Xüsusi DT
// undefined
var infoUn = undefined;
console.log(infoUn);
// null
var infoNull = null;
console.log(infoNull);
